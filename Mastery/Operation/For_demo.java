
public class For_demo {
    public static void main(String[] args) {
        System.out.println("LOOPS");

        // for loop
        for(int i=1;i<10;i++){
            if(i%2==0){
                System.out.println(i + " is a multiple of 2");

            }

        }

        //while loop
        System.out.println("While loop");
        int i=1;
        while(i<5){
            System.out.println(i);
            i=i+1;
        }
    }
    
}
